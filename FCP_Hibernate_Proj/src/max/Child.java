package max;

public class Child
{
	int cid;
	String cname;
	String age;
	String cadhar;
	
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getCadhar() {
		return cadhar;
	}
	public void setCadhar(String cadhar) {
		this.cadhar = cadhar;
	}
	
}
