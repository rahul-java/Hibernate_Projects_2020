package shgregistration;

public class SHGMemberDetail
{
	long memeberCode;
	 String cname;
     String age;
     String cadhar;
     
	
	
	public long getMemeberCode() {
		return memeberCode;
	}
	public void setMemeberCode(long memeberCode) {
		this.memeberCode = memeberCode;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getCadhar() {
		return cadhar;
	}
	public void setCadhar(String cadhar) {
		this.cadhar = cadhar;
	}
     
}
